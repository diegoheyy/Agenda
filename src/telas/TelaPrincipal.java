    private void bt_pesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisaActionPerformed
        List<Contato> show = null;
        bt_excluir.setEnabled(false);
        try {
            show = pesquisaContato(jt_pesquisa.getText());
            DefaultTableModel tab = (DefaultTableModel) tab_Contato.getModel();
            tab.setNumRows(0);

            for (Contato com : show) {
                tab.addRow(com.infoBasico());
            }
        } catch (Exception ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_bt_pesquisaActionPerformed

