    private void bt_editarcontatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarcontatoActionPerformed
        if (tab_Contato.getSelectedRow() == -1) {
            return;
        }
        int linha = tab_Contato.getSelectedRow();
        int cod = Integer.parseInt((String) tab_Contato.getValueAt(linha, 4));
        Contato pessoa = null;
        try {
            pessoa = Consultas.pesquisaContato(cod);
        } catch (Exception ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        TelaCadastro cadastro = new TelaCadastro(pessoa);
        
        cadastro.setVisible(true);
        
    }//GEN-LAST:event_bt_editarcontatoActionPerformed

