    private void carregaInfo(Contato c) {
        List<Email> em = c.getEmails();
        DefaultTableModel tab = (DefaultTableModel) tab_email.getModel();
        tab.setNumRows(0);
        for (Email com : em) {
            tab.addRow(com.infoBasico());
        }

        List<Fone> fo = c.getFones();
        DefaultTableModel tab1 = (DefaultTableModel) tab_fone.getModel();
        tab1.setNumRows(0);
        for (Fone co : fo) {
            tab1.addRow(co.infoBasico());
        }
    }

    private void preparaCombo() {
        String[] tipoe = Consultas.vetTipoEmail();
        for (String desce : tipoe) {
            box_email.addItem(desce);
        }
        String[] tipof = Consultas.vetTipoFone();
        for (String descf : tipof) {
            box_fone.addItem(descf);
        }
    }

    private List<Fone> novosFones() {
        DefaultTableModel tab = (DefaultTableModel) tab_fone.getModel();

        List<Fone> lista = new ArrayList<>(0);

        for (int linha = 0; linha < tab.getRowCount(); linha++) {
            TipoFone tipo = new TipoFone(Integer.parseInt((String) tab.getValueAt(linha, 2)), (String) tab.getValueAt(linha, 1));

            lista.add(new Fone((String) tab.getValueAt(linha, 0), tipo));
        }

        return lista;
    }

    private List<Email> novosEmails() {
        DefaultTableModel tab = (DefaultTableModel) tab_email.getModel();

        List<Email> lista = new ArrayList<>(0);

        for (int linha = 0; linha < tab.getRowCount(); linha++) {
            TipoEmail tipo = new TipoEmail(Integer.parseInt((String) tab.getValueAt(linha, 2)), (String) tab.getValueAt(linha, 1));
            lista.add(new Email((String) tab.getValueAt(linha, 0), tipo));
        }

        return lista;
    }

