    private void bt_gravAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_gravAltActionPerformed
        if ("".equals(tex_nome.getText().trim()) || "".equals(tex_sobrenome.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Nome e/ou sobrenome nao pode estar vazio.");
            return;
        }
        
        try {
            contatoAtual.setNome(tex_nome.getText());
            contatoAtual.setSobrenome(tex_sobrenome.getText());
            contatoAtual.setEmails(novosEmails());
            contatoAtual.setFones(novosFones());
            Consultas.alteraContato(contatoAtual);
            JOptionPane.showMessageDialog(null, "Sucesso");
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_bt_gravAltActionPerformed

    private void bt_gravNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_gravNovoActionPerformed
        Contato contato = new Contato();
        if ("".equals(tex_nome.getText().trim()) || "".equals(tex_sobrenome.getText().trim())) {
            JOptionPane.showMessageDialog(null, "Nome e/ou Nao pode estar vazio");
            return;
        }

        DefaultTableModel tabfone = (DefaultTableModel) tab_fone.getModel();
        List<Fone> fones = new ArrayList<Fone>(0);
        for (int linha = 0; linha < tabfone.getRowCount(); linha++) {
            TipoFone tipo = new TipoFone();
            //       tabfone.getValueAt(linha, 2);
            tipo.setCod(Integer.parseInt((String) tabfone.getValueAt(linha, 2)));
            String numero = (String) tabfone.getValueAt(linha, 0);
            fones.add(new Fone(numero, tipo));
        }

        DefaultTableModel tabemail = (DefaultTableModel) tab_email.getModel();
        List<Email> emails = new ArrayList<Email>(0);
        for (int linha = 0; linha < tabemail.getRowCount(); linha++) {
            TipoEmail tipo = new TipoEmail();
            tipo.setCod(Integer.parseInt((String) tabemail.getValueAt(linha, 2)));
            String emai = (String) tabemail.getValueAt(linha, 0);
            emails.add(new Email(emai, tipo));
        }

        contato.setNome(tex_nome.getText());
        contato.setSobrenome(tex_sobrenome.getText());
        contato.setFones(fones);
        contato.setEmails(emails);

        try {
            Consultas.insereContato(contato);

            JOptionPane.showMessageDialog(null, "sucesso");
           // TelaPrincipal.contatosNaTabela();
        } catch (Exception ex) {
            Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }

        dispose();
    }//GEN-LAST:event_bt_gravNovoActionPerformed

